   var baseURL = location.origin.indexOf('.com') > -1 ? location.origin : location.origin + '/reapweb'
   var msgTimer = null;

   function showError(msg, autoHide = true) {
      if (msgTimer) {
         clearTimeout(msgTimer)
      }
      $('.flash-msg').removeClass('success')
      $('.flash-msg').addClass('error')
      $('.res-msg').text(msg)
      if (autoHide) {
         msgTimer = setTimeout(() => {
            clearMessage()
         }, 5000);
      }
   }

   function showSuccess(msg) {
      if (msgTimer) {
         clearTimeout(msgTimer)
      }
      $('.flash-msg').removeClass('error')
      $('.flash-msg').addClass('success')
      $('.res-msg').text(msg)
   }

   function clearMessage() {
      $('.flash-msg').removeClass('error')
      $('.flash-msg').removeClass('success')
   }
</script>